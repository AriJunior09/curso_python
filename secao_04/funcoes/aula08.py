""" Retorno das funções:
- Sempre que uma função não retorna um valor, o Python retorna None.
- Funções que retornam valores, retornam o valor especificado.
- Funções podem retornar múltiplos valores, na forma de tuplas.
 """


def soma(a, b):
    return a + b

soma1 = soma(2, 2)
soma2 = soma(3, 3)

print(soma1 + soma2)