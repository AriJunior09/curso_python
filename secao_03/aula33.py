"""
Operadores de atribuição
= += -= /= //= **= %=
"""

a = b = c = d = e = f = g = 10

a += 1
b -= 1
c *= 2
d /= 2
e //= 3
f **= 2
g %= 2

print(f'10 + 1 = {a}')
print(f'10 - 1 = {b}')
print(f'10 * 2 = {c}')
print(f'10 / 2 = {d}')
print(f'10 // 3 = {e}')
print(f'10 ** 2 = {f}')
print(f'10 % 2 = {g}')


