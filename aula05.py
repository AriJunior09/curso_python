print('Adição', adicao)

subtracao = 10 - 5
print('Subtração', subtracao)

multiplicacao = 10 * 10
print('Multiplicação', multiplicacao)

divisao = 10 / 3          # Resultado sempre float
print('Divisao', divisao)

divisao_inteira = 10 // 3 # Resultado arredondado para baixo, sempre inteiro
print('Divisao inteira', divisao_inteira)

exponenciacao = 2 ** 10   # 2 elevado a 10
print('Exponenciacao', exponenciacao)

modulo = 25 % 2 # Resto da divisão
print('Modulo', modulo)

